# @param none
class Rewriting
  def initialize
    @old_file = './temp.md'
    @old_txt = "！シーズンID！"
    @new_file = './temp2.md'
  end

  def rewriting
    f = File.open(@old_file, "r")
    p f
    in_sh = f.read
    f.close

    # 置換処理
    index = 0
    out_sh = in_sh.gsub(@old_txt) do
      index += 1
      "#{"！シーズンID！".gsub(/！シーズンID！/, index.to_s)}"
    end
    
    
    f = File.open(@new_file, "w")
    f.write(out_sh)
    f.close
  end
end
